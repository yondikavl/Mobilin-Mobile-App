class Car {
  final String id;
  final String name;
  final String description;
  final double pricePerDay;
  final String imagePath;
  final int seats;
  final String speed;

  Car({
    required this.id,
    required this.name,
    required this.description,
    required this.pricePerDay,
    required this.imagePath,
    required this.seats,
    required this.speed,
  });
}
